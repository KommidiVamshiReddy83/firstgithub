public class ForLoop {

    public static void main(String[] args) {
        for (int i=0;i<=5;i++){//for (statement 1 ,statement 2 ,statement3)
            System.out.print(i);
        }
        System.out.println();
        //TO PRINT THE EVEN NUMBERS FROM 0 TO 100
        for (int i=0;i<=100;i+=2){
            System.out.print(i +" ");
        }
        System.out.println();

        for (int i=1; i<=10;i++){
            System.out.print(i);
        }

    }
}
